function toSlug(title: string): string {
  return title
    .toLowerCase()
    .replace(/[^a-z0-9가-힣\s-]/g, "")
    .replace(/\s+/g, "-")
    .slice(0, 50)
    || "landing-page";
}

export async function POST(req: NextRequest) {
  let body: ExportBody;
  try {
    body = await req.json();
  } catch {
    return Response.json(
      { error: "Invalid JSON" },
      { status: 400 },
    );
  }

  const { html, title } = body;
  if (!html || !title) {
    return Response.json(
      { error: "html and title are required" },
      { status: 400 },
    );
  }

  const slug = toSlug(title);
  const prefix = `${slug}/`;

  // Create zip via archiver → PassThrough → ReadableStream
  const passthrough = new PassThrough();
  const archive = archiver("zip", { zlib: { level: 9 } });
  archive.pipe(passthrough);

  // Add project files
  archive.append(packageJson(title), { name: `${prefix}package.json` });
  archive.append(tsconfig, { name: `${prefix}tsconfig.json` });
  archive.append(nextConfig, { name: `${prefix}next.config.mjs` });
  archive.append(postcssConfig, { name: `${prefix}postcss.config.mjs` });
  archive.append(readme(title), { name: `${prefix}README.md` });
  archive.append(globalsCss, { name: `${prefix}src/app/globals.css` });
  archive.append(layoutTsx(title), { name: `${prefix}src/app/layout.tsx` });
  archive.append(pageTsx(html), { name: `${prefix}src/app/page.tsx` });
  archive.append(".next\nnode_modules\n", { name: `${prefix}.gitignore` });

  archive.finalize();

  // Convert Node stream to Web ReadableStream
  const readable = new ReadableStream({
    start(controller) {
      passthrough.on("data", (chunk: Buffer) => controller.enqueue(chunk));
      passthrough.on("end", () => controller.close());
      passthrough.on("error", (err) => controller.error(err));
    },
  });

  return new Response(readable, {
    headers: {
      "Content-Type": "application/zip",
      "Content-Disposition": `attachment; filename="${slug}.zip"`,
    },
  });
}
