function pageTsx(html: string) {
  // Escape backticks and ${} in the HTML for template literal safety
  const escaped = html
    .replace(/\\/g, "\\\\")
    .replace(/`/g, "\\`")
    .replace(/\$\{/g, "\\${");

  return `export default function Home() {
  return (
    <div
      dangerouslySetInnerHTML={{
        __html: \`${escaped}\`,
      }}
    />
  );
}
`;
}

